package small_study.java.smallstudy01;

public class Loop20Ekim {
    public static void main(String[] args) {

        // 1 den 10 a kadar elemanlari yazdiralim

        for (int i = 1; i < 11; i++) {

            System.out.print(i + " ");
        }

        System.out.println();

        int sum = 0;

        for (int i = 0; i < 11; i++) {

            sum += i;
        }

        System.out.print(sum + " ");


    }
}





