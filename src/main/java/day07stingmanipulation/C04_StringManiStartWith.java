package day07stingmanipulation;

public class C04_StringManiStartWith {
    public static void main(String[] args) {
/**
 * startsWith()
 *  Metnin bas kisminin istenilen dizi ile eslesip eslesmedigini kontrol eder. Sonucu boolean dir.
 *
 * endsWith()
 * Metnin son kisminin istenilen dizi ile eslesip eslesmedigini kontrol eder. Sonucu boolean dir.
 */
        String cumle="Kazananlar hic hata yapmayanlar degil,vazgecmeyenlerdir";

        System.out.println(cumle.startsWith("K"));  //Bu tarz sorularda hep boolean doner.
        System.out.println(cumle.endsWith("dir"));














    }
}
